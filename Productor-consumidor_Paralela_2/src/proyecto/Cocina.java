    private void botonInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonInicioActionPerformed
        // TODO add your handling code here:
        ChefCliente pc = new ChefCliente(pizza0, pizza1, pizza2, pizza3, pizza4, pizza5, pizza6, pizza7, pizza8, estadoChef, estadoJoey, chefdurmiendo, joeyEsperando);
        Thread productor = new Thread(() -> {
            try {
                pc.producir();
            } catch (InterruptedException ex) {
                Logger.getLogger(ChefCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        });

        Thread consumidor = new Thread(() -> {
            try {
                pc.consumir();
            } catch (InterruptedException ex) {
                Logger.getLogger(ChefCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        });

        consumidor.start();
        productor.start();
    }//GEN-LAST:event_botonInicioActionPerformed

