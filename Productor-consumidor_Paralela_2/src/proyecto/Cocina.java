    public void setImageLabel(JLabel chef1, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon = new ImageIcon(
                image.getImage().getScaledInstance(chef1.getWidth(), chef1.getHeight(), Image.SCALE_DEFAULT)
        );
        chef1.setIcon(icon);
        this.repaint();
    }

